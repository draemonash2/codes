int main(void)
{
	/* 変数宣言 */
	int in;		/* ユーザ入力データ */
	int loop;	/* ループ可否 */
	
	/* 変数初期化 */
	loop = 1;
	
	/* ループ */
	do {
		/* ユーザ入力 */
		printf("何階に行きますか？（１〜３）>");
		scanf("%d", &in);
		
		/* 入力判定と結果表示 */
		switch (in) {
		case 1:
			printf("化粧品売り場です\n");
			break;
		case 2:
			printf("紳士服売り場です\n");
			break;
		case 3:
			printf("婦人服売り場です\n");
			break;
		default:
			printf("その階には行けません\n");
			loop = 0;
			break;
		}
	} while (loop);
	
	/* 結果return */
	return 0;
	
}
