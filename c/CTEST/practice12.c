/* ヘッダファイル読み込み */
#include <stdio.h>

/*======================================================================
	関数名			：main
	機能			：メイン処理
	入力引数説明	：None
	出力引数説明	：None
	戻り値			：終了情報（常に０）
	入力情報		：None
	出力情報		：None
	特記事項		：None
	修正履歴		：1.00	2010/2/4	研修 太郎	作成
======================================================================*/

int main(void)
{
	/* 変数定義 */
	int in;		/* ユーザ入力データ */
	
	/* ユーザ入力 */
	printf("何階に行きますか？（１〜３）>");
	scanf("%d", &in);
	
	/* 1階：化粧品売り場
	   2階：紳士服売り場
	   3階：婦人服売り場 */
	
	/* 入力データ判定と結果表示 */
	switch (in) {
		case 1:
			printf("化粧品売り場です\n");
			break;
		case 2:
			printf("紳士服売り場です\n");
			break;
		case 3:
			printf("婦人服売り場です\n");
			break;
		default:
			printf("その階には行けません\n");
			break;
	}
	
	/* 結果return */
	return 0;
	
}
