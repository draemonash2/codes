int main(void)
{
	/* 変数宣言 */
	int in;		/* ユーザ入力データ */
	
	/* ユーザ入力 */
	printf("何階に行きますか？（１〜３）>");
	scanf("%d", &in);
	
	/* 1階：化粧品売り場
	   2階：紳士服売り場
	   3階：婦人服売り場 */
	
	/* 入力データ判定と結果表示 */
	if (in == 1) {
		printf("化粧品売り場です\n");
	} else if (in == 2) {
		printf("紳士服売り場です\n");
	} else if (in == 3) {
		printf("婦人服売り場です\n");
	} else {
		printf("その階には行けません\n");
	}
	
	/* 結果return */
	return 0;
	
}
